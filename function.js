//#################################################################################################################
// + Get Current Time and Convert to right date format
//=================================================================================================================
//	 - Attributes :
//			format : format that going to output
//				"DB_date" 	: Database format for "date" type of data.
//				"DB_datetime" 	: Database format for "datetime" or "timestamp" data
//=================================================================================================================
// + Return : Current time in requested format
//#################################################################################################################
function GetCurrentTime(format){
	
	//==============================================================================================
	// + Create Time
	//==============================================================================================
	var t = new Date();
	
	//==============================================================================================
	// + Convert Time
	//==============================================================================================
	//----------------------------------------------
	// + To Database - date Format (YYYY-MM-DD)
	//----------------------------------------------
	if(format == "DB_date"){return t.getFullYear()+"-"+Az(t.getMonth()+1)+"-"+Az(t.getDate());}
	
	//----------------------------------------------
	// + To Database - datetime / Timestamp Format (YYYY-MM-DD HH:MI:SS)
	//----------------------------------------------
	else if(format == "DB_datetime"){return t.getFullYear()+"-"+Az(t.getMonth()+1)+"-"+Az(t.getDate())+" "+Az(t.getHours())+":"+ Az(t.getMinutes())+":"+Az(t.getSeconds());}
	
}

//#################################################################################################################
// + Add Zero to the single digit
//=================================================================================================================
//
//#################################################################################################################
function Az(num){
	if(num < 10){return "0"+num}
	else{return num}
}
